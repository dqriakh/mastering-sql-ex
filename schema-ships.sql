-- ======================================================
-- Схема базы данных "Корабли" (Вторая мировая война)
-- Для заданий с сайта sql-ex.ru
-- ======================================================

-- ======================================================
-- Удаление существующих таблиц (если они есть)
-- ======================================================
DROP TABLE IF EXISTS Outcomes;
DROP TABLE IF EXISTS Battles;
DROP TABLE IF EXISTS Ships;
DROP TABLE IF EXISTS Classes;

-- ======================================================
-- Создание таблицы Classes (классы кораблей)
-- ======================================================
CREATE TABLE Classes (
    class VARCHAR(50) PRIMARY KEY,      -- Имя класса
    type VARCHAR(2) NOT NULL,            -- Тип: 'bb' - боевой корабль, 'bc' - боевой крейсер
    country VARCHAR(50) NOT NULL,        -- Страна-производитель
    numGuns INT,                          -- Количество главных орудий
    bore INT,                             -- Калибр орудий (дюймы)
    displacement INT                      -- Водоизмещение (тонны)
);

-- ======================================================
-- Создание таблицы Ships (корабли)
-- ======================================================
CREATE TABLE Ships (
    name VARCHAR(50) PRIMARY KEY,        -- Название корабля
    class VARCHAR(50) NOT NULL,           -- Класс корабля
    launched INT,                          -- Год спуска на воду
    FOREIGN KEY (class) REFERENCES Classes(class)
);

-- ======================================================
-- Создание таблицы Battles (сражения)
-- ======================================================
CREATE TABLE Battles (
    name VARCHAR(50) PRIMARY KEY,        -- Название сражения
    date DATE NOT NULL                     -- Дата сражения
);

-- ======================================================
-- Создание таблицы Outcomes (результаты сражений)
-- ======================================================
CREATE TABLE Outcomes (
    ship VARCHAR(50) NOT NULL,            -- Название корабля
    battle VARCHAR(50) NOT NULL,          -- Название сражения
    result VARCHAR(10) NOT NULL,           -- Результат: 'sunk', 'damaged', 'OK'
    PRIMARY KEY (ship, battle),
    FOREIGN KEY (battle) REFERENCES Battles(name)
    -- Примечание: внешний ключ на ship не создаем, так как в Outcomes
    -- могут быть корабли, отсутствующие в таблице Ships
);
